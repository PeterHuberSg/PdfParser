    private static char[] getEmptyEncoding() {
      var encoding = new char[0x100];
      Array.Fill<char>(encoding, '\uFFFF');
      return encoding;
    }


    private static void add(char[] encoding, char ch, string part) {
      if (part!="—") {
        var charIndex = Convert.ToInt32(part, 8);
        if (encoding[charIndex]!='\uFFFF') throw new Exception();

        encoding[charIndex] = ch;
      }
    }


    private static void createCode(StringBuilder sb, string encodingName, char[] encoding) {
      sb.AppendLine();
      sb.AppendLine();
      sb.AppendLine($"      public static char[] {encodingName} = {{");
      sb.Append("        ");
      for (int charIndex = 0; charIndex < encoding.Length; charIndex++) {
        sb.Append($"'\\u{(int)encoding[charIndex]:X4}', ");
        if (charIndex % 8 == 7) {
          sb.AppendLine();
          sb.Append("        ");
        }
      }
      sb.Length = sb.Length - 2;
      sb.AppendLine("};");
    }
